char	*ft_strcat(char *s1, const char *s2)
{
	int	s;
	int	d;

	s = 0;
	d = 0;
	while (s1[d])
		d++;
	while (s2[s])
	{
		s1[d] = s2[s];
		d++;
		s++;
	}
	s1[d] = '\0';
	return (s1);
}
