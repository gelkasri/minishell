char	*ft_strupcase(char *str)
{
	int		diff;
	int		i;

	i = 0;
	diff = 'A' - 'a';
	while (str[i] != '\0')
	{
		str[i] = (str[i] >= 'a' && str[i] <= 'z') ? str[i] + diff : str[i];
		i++;
	}
	return (str);
}
